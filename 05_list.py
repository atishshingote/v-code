# create a list using []
a = [1, 2, 3, 4, 56, 6]

# print the list using print() function
print(a)

# access using index using a[0], a[1], a[2]
print(a[2])

# change the value of list using
a[0] = 98
print(a)

# we can create a list with items of different types
c = [45, "atish", False, 6.9]
print(c)

# list slicing
frinds =["rohit","sonu","rahul","adesh",45]
print(frinds[0:4])
print(frinds[-4:])